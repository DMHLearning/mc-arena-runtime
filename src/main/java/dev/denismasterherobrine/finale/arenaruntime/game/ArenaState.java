package dev.denismasterherobrine.finale.arenaruntime.game;

public enum ArenaState {
    /** Арена свободна и ожидает игроков. Мира может еще не существовать. */
    IDLE,
    /** Инфраструктура готовится (AWM копирует мир, ставит схематики). */
    PREPARE,
    /** Идет игра: спавнятся мобы, тикают таймеры. */
    RUNNING,
    /** Игра окончена (победа/поражение/эвакуация), выдаются награды. */
    FINISH,
    /** Арена сбрасывается (AWM удаляет мир), игроки телепортированы в лобби. */
    RESET
}
